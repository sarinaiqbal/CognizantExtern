num1 = 78
num2 = 59

# Add the numbers and store the sum in variable addition
addition = num1 + num2
print("The sum of 78 and 59 is", addition)

# Subtract the numbers and store the result in variable subtraction
subtraction = num1 - num2
print("The result of 78 and 59 is", subtraction)

# Multiply the numbers and store the product in variable multiplication
multiplication = num1 * num2
print("The product of 78 and 59 is", multiplication)

# Divide (floor division) the numbers and store the quotient in variable division
division = num1 / num2
print("The quotient of 78 and 59 is", division)
