# creating list
fave_fruit = ["apple", "pear", "banana", "cherry", "mango"]
print("Original list:", fave_fruit)

# adding a fruit
fave_fruit.append("watermelon")
print("After adding a fruit:", fave_fruit)

# removing a fruit
fave_fruit.remove("pear")
print("After removing a fruit:", fave_fruit)

# reversing list
print("Reversed list:", fave_fruit[::-1])
